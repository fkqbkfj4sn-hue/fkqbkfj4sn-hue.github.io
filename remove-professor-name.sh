#!/bin/bash

echo "开始移除'吴忠明教授'字样并更新网站内容..."

# 1. 备份index.html
cp index.html "index.html.backup.$(date +%Y%m%d_%H%M%S)"

# 2. 移除研究概述中的"吴忠明教授"字样（使用通用方法）
echo "更新研究概述..."
sed -i 's|吴忠明教授||g' index.html

# 3. 更新研究概述内容
echo "更新研究概述内容..."
sed -i 's|本网站系统整理了关于剩余劳动价值论的完整研究历程，所有成果均基于可验证的学术证据，旨在建立公开、透明、可验证的学术研究数字档案。|本数字实验中心系统整理了关于剩余劳动价值论的完整研究历程，所有成果均基于《资本论》模型与世界银行数据库的实证分析，旨在建立公开、透明、可验证的学术研究数字实验平台。|' index.html

# 4. 更新副标题
echo "更新副标题..."
sed -i 's|基于可验证证据的完整学术研究历程存档（2007-2026）|基于《资本论》模型与世界银行数据库的科研平台（2007-2026）|' index.html

# 5. 检查是否还有其他地方需要移除
echo ""
echo "检查结果："
echo "1. 研究概述中的'吴忠明教授'："
if grep -I -q "吴忠明教授" index.html; then
    echo "❌ 仍有残留"
else
    echo "✅ 已移除"
fi

echo ""
echo "2. 更新后的研究概述："
grep -n "本数字实验中心系统整理了关于剩余劳动价值论的完整研究历程" index.html

echo ""
echo "3. 更新后的副标题："
grep -n "基于《资本论》模型与世界银行数据库的科研平台" index.html

echo ""
echo "✅ 修改完成！"
